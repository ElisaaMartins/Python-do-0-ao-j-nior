# listas podem ter varios tipos de variaveis
lista = [1, 2, 3]

lista [0] = 6 # alterando o valor da posição 1 da lista

print(lista[0])
print(lista[1])
print(lista[2])


# lista vazia, com o método append pode colocar valores na lista depois
listaVazia = []
 
listaVazia.append("Olá")
listaVazia.append("Mundo")

print(listaVazia)
